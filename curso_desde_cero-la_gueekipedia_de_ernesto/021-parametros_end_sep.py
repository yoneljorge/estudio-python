"""
El parámetro end, se utiliza para agregar cualquier cadena de caracteres al 
final de la salida e impresión en pantalla de la función print().
- Además, por defecto la función print() genera un salto de línea al terminar 
su ejecución, sin embargo, con ayuda del parámetro end, es posible evitar este salto de línea.
"""

print("Esto es un", end="-*/")
print("ejemplo")

"""
El parámetro sep, se utiliza para dar formato a las cadenas de caracteres que deben imprimirse 
en pantalla, agregando un separador entre las cadenas que se imprimirán.
"""

print("1", "2", "3","4","5", sep="-")